public class MiaClass {
    public static void main(String args[]) {
      int x = 1;
      int y = 3;
      double z = (double) x / (double) y;
      float zFloat = (float) x / (float) y;

      System.out.println("Division of x/y with double = " + z);
      System.out.println("Division of x/y with float = " + zFloat);
    }
}
